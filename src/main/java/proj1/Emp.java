package proj1;

public class Emp {
    private String name;
    private String surname;
    private int age;
    private int experienceCount;
    private String role;

    private String getName() {
        return name;
    }
    private String getSurname() {
        return surname;
    }
    private int getAge() {
        return age;
    }
    private int getExperienceCount() {
        return experienceCount;
    }
    private String getRole() {
        return role;
    }

}
